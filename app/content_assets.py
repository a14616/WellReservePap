def _keyword_bucket(text, buckets, default_key):
	text = (text or '').lower()
	for key, keywords in buckets:
		if any(keyword in text for keyword in keywords):
			return key
	return default_key


def produto_assets(produto):
	name = getattr(produto, 'nome', '') or ''
	if name in PRODUCT_IMAGE_BY_NAME:
		assets = PRODUCT_IMAGE_BY_NAME[name]
		return {
			'cover': assets[0],
			'gallery': assets[1:3],
		}

	texto = ' '.join([
		getattr(produto, 'nome', '') or '',
		getattr(getattr(produto, 'categoria', None), 'nome', '') or '',
		getattr(getattr(produto, 'categoria', None), 'descricao', '') or '',
	])
	bucket = _keyword_bucket(texto, [
		('acesso', ['pulseira', 'acesso', 'rfid', 'check-in', 'entrada']),
		('packs', ['voucher', 'pack', 'experi', 'presente']),
		('planos', ['plano', 'assinatura', 'mensal', 'subscri']),
		('hidratacao', ['garrafa', 'hidrata', 'água', 'agua', 'termica', 'isotérmica', 'isotermica']),
		('funcional', ['banda', 'elást', 'elast', 'rolo', 'treino', 'funcional']),
		('apoio', ['kit', 'apoio', 'organiza', 'locker', 'saco']),
		('merchandising', ['t-shirt', 'camis', 'boné', 'bone', 'toalha', 'robe', 'marca']),
	], 'recuperacao')
	assets = PRODUCT_IMAGE_ASSETS[bucket]
	return {
		'cover': assets[0],
		'gallery': assets[1:4],
	}


